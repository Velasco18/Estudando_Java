
public class MathExponenciacaoPotenciacao {

	/* Calcular a Exponenciação e também a Potenciação de um número inteiro utilizando a classe Math e respectivamente os
	 *  métodos: Math.exp e Math.pow */
	public static void main(String[] args) {
		int num = 2;
		
		
		/* Apresentação da Exponenciação de um número inteiro elevado ao quadrado, utilizando o método exp da classe Math */
		System.out.println("exp: valor de E elevado ao quadrado: " + Math.sqrt(2));
		
		/* Apresentação da Potenciação de um número inteiro elevado à quarta, utilizando 
		 * o método pow da classe Math */
		System.out.println("pow: "+ num + " - elevado a quarta = " + Math.sqrt(4));

	}

}
